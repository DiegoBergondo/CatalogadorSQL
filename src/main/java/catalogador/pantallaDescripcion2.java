    /**
     * Asigna al jLabel2 una imagen dependiendo del valor que llega como parámetro.
     * @param i
     */
    private void cambiarIcon(int i){
        if(i==0)
            this.jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/sad.png")));
        else if(i==1)
            this.jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/sick.png")));
        else if(i==2)
            this.jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/confused.png")));
        else
            this.jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/in-love.png")));
    }

