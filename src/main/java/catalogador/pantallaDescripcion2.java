                this.cambiarIcon(0);
        if(this.getEstado().getSelectedItem().toString().equalsIgnoreCase("Gastado"))
                this.cambiarIcon(1);
        if(this.getEstado().getSelectedItem().toString().equalsIgnoreCase("Aceptable"))
                this.cambiarIcon(2);
        else if(this.getEstado().getSelectedItem().toString().equalsIgnoreCase("Nuevo"))
                this.cambiarIcon(3);
